namespace CalculatorOOP
{
    public class NegativeNumberCalculate : IOneArgumentCalculate
    {
        public double OneArgCalculate(double number)
        {
            /// <summary>
            /// this function calculate negaive of first argument 
            /// </summary>
            /// <param name="number"></param>
            /// <returns></returns>
            return -number;
        }
    }
}


